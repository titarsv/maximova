                        <p>
                            Most consumers do not grasp the difference between the price and the value of a product or service. Price is simply the amount of money paid or charged for something. When we focus on price, we are focusing on <a href="#"><strong>the short-term acquisition</strong></a> of a product. Value, on the other hand, focuses on the long-term aspect of the purchase.
                        </p>
                        <p>
                            Price is what a buyer spends, and value is what they receive in the transaction. When a buyer has received more value from a product than what they spent, this purchase is viewed as possessing great value. If a buyer values your product and can find a solution to his problem with your product more than he values his money, then he will purchase your product. People who focus on cost focus on the total cost of ownership, but people who focus on value focus on the total picture and how the product will create a solution.
                        </p>
                        <p>
                            Now, how can we compute value in real estate and specifically multifamily real estate? There are basically three methods of calculating real estate value: the cost approach, the sales approach, and the income approach. The sales approach is widely used in valuing single family homes, and the cost approach is utilized for properties that have few comps and for new properties (such as a church or school). Let’s focus on the income method, which utilizes the net operating income and cap rates to determine the property’s value. This is by far the best method to analyze apartments.
                        </p>
                        <h2>
                            Hyperbolic Discounting
                        </h2>
                        <p>
                            Before we dive into analyzing the value of a multifamily property, I would like to discuss the term “ <a href="#">hyperbolic discounting</a>” and why I think a significant amount of investors shy away from investing in multis. I was introduced to this term by Gary Keller while reading the book The One Thing, and hyperbolic discounting states that the farther away a reward is, the less motivated an individual is to achieve it. If I have a choice of earning $100 in two weeks or earning $500 in 18 months, most people will choose the present reward over the future reward overwhelmingly. This impulse of instant gratification is becoming evermore popular within our society.
                        </p>
                        <p>
                            This may explain why strategies such as wholesaling and fix and flipping are extremely popular to investors. These strategies employ much shorter time horizons than multifamily investments. A wholesaler can earn a profit in a matter of weeks, while a multifamily investor usually needs to dedicate a much longer time horizon to execute his business plan to generate his return.
                        </p>
                        <p>
                            There are other challenges that investors encounter when deciding upon multifamily investments, such as lack of capital or lack of experience, but I feel that not being able to focus on the long-term dissuades many investors from multifamily investing. I sometimes wonder if our society is losing the willpower and the persistence to see things through.
                        </p>
                        <p>
                            If you understand the value and the various benefits that multifamily offers, the decision of delayed gratification will be a no-brainer. So what are the benefits of multifamily, and how do we determine the value?
                        </p>
                        <img src="/wp-content/themes/maximova/images/articles/5.jpg" alt="">
                        <h2>
                            6 Benefits of Investing in Multifamily Real Estate
                        </h2>
                        <p>
                            Here is a list of benefits:
                        </p>
                        <ul class="numeric">
                            <li>
                                <strong>Cash flow. </strong>Apartments generate monthly income, what I like to refer to as wallet money. I compare cash flow to dividends paid by stocks. The money rolls in every month.
                            </li>
                            <li>
                                <strong>Control. </strong>You are the captain of your own ship. You have the ability to control every decision that affects your investment.
                            </li>
                            <li>
                                <strong>Tax advantages. </strong>It’s not what you make, it’s what you keep that’s important, and real estate offers tremendous tax benefits. Why would the government create advantages for this tax class? The government realizes it does not have the ability to deliver affordable housing, and by offering these benefits, it is trying to stimulate the private sector to step in and fill the void.
                            </li>
                            <li>
                                <strong>Economy of scale: </strong>This is a huge advantage when trying to scale your business. I find it much easier trying to collect rent from 30 tenants in my apartment building rather than running all across the city to collect from my single family homes. It is easier and more cost effective to have more units under one roof.
                            </li>
                            <li>
                                <strong>Ability to force the appreciation: </strong>The value is not as reliant on comps as it is your ability to increase the value through growing the NOI.
                            </li>
                        </ul>
